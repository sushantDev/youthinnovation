                <h2>Community Impact Hub</h2>
                <p>
                    Community Impact Hubs is a volunteer initiative of YI-Lab to promote culture of entrepreneurship and innovation in grassroot level. The formal and informal youth groups like Academic institutions, Community Based Organisations and youth groups can register their local chapters and organise events.
                </p>
                <p>
                    With a population of estimated 31 million, Nepal is a highly stratified society with 125 caste and ethnic groups (including 63 indigenous peoples). Nepal is ranked 145th out of 188 countries on United Nations Development Program’s 2015 Human Development Index, placing the country in the low human development category. GNI per capita is estimated at US $730. 15% of people are living on less than $1.90 per day.

                    According to World bank’s 2016 report, about 80% of total population of Nepal lives in rural community. Nepal is essential an agrarian economy, providing subsistence livelihood for 70% of its population. The structural transition from agriculture towards knowledge and technology sectors has been slow. The economy does not have the capacity to create employment for all those entering the labor market. The lack of economic opportunity and poverty has fuelled the migration of an estimated three million Nepali workers to various countries. Remittance amounts to 25% of GDP.
                    It shows that there is dearth of local level platform that gives opportunities to youths to understand their local problem and initiate further to solve it. Community Impact Hubs is envisioned to fill the gap to recognise their challenges and solve it under their own leadership.

                <p>
                    Community Impact Hubs is a community level platform envisioned to engage local youths to have discussion about the local problems and find solutions. Any formal and informal  local youth group can simply register their platform in YI-Lab’s website following the Community Impact Hubs registration guideline.
                    After successful registration, the Community Impact Hubs will get resources like Impact grants, organisation registration process, funding opportunities, reporting and financial management guidelines, and mentorship.

                    To be registered as the Community Impact Hubs, the local group will be charged Rs. 10,000 and annually, Rs. 5000 to continue its membership and services.

